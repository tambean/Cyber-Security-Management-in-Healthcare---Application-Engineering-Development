   public void populatePatientTable(){
        
        DefaultTableModel dtbl = (DefaultTableModel) tblPatient.getModel();
        dtbl.setRowCount(0);       
        
        if (org != null) {
            for (Person person : enterprise.getPersonDirectory().getPersonDir()) {
                Object[] row = new Object[2];
                row[0] = person;
                row[1] = person.getPersonName();
                dtbl.addRow(row);
            }
      
        }   
    }
    
