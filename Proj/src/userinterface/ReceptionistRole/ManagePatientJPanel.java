    private void btnUpdateInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateInfoActionPerformed
        
         int selectedRow = tblPatient.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null,"Please select row from table");
            return;
        }
        tblPatient.getValueAt(selectedRow, 0);
        Person p =(Person)  tblPatient.getValueAt(selectedRow, 0);
        UpdatePatientInformationJPanel updatePatient = new UpdatePatientInformationJPanel(userProcessContainer, org,p);
        userProcessContainer.add("updatePatient", updatePatient);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnUpdateInfoActionPerformed

    private void btnAppointmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAppointmentActionPerformed
        
        int selectedRow = tblPatient.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select row from table");
            return;
        }    
        Person p = (Person)tblPatient.getValueAt(selectedRow,0);
        RequestBookingAppointmentJPanel requestBooking = new RequestBookingAppointmentJPanel(userProcessContainer, account,enterprise,p);
        userProcessContainer.add("requestBooking", requestBooking);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnAppointmentActionPerformed

